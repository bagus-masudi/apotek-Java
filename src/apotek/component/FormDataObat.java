    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try{
            if(nama.getText().equals("")|| ID.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong","Pesan", JOptionPane.ERROR_MESSAGE);
                hapuslayar();
            }else{
                try {
                    Connection con = connectionmysql.koneksiDB();
                    String sql = "insert into obat values ('"+ID.getText()+"','" +KD_GOLONGAN.getSelectedItem() +"','"+nama.getText()+"','"+jenis.getText()+"','"+harga.getText()+"','"+stok.getText()+"','"+ket.getText()+"')" ;
                    Statement S = con.createStatement();
                    S.execute(sql);
                    JOptionPane.showMessageDialog(null, "Penambahan Berhasil");
                    tampilkanDataObat();
                } catch (Exception e) {
                    System.out.println(e);
        }

            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, "Penambahan Gagal","Pesan", JOptionPane.WARNING_MESSAGE);
            //hapuslayar();
        }
    }//GEN-LAST:event_saveActionPerformed

    private void tabelObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelObatMouseClicked
        int baris = tabelObat.rowAtPoint(evt.getPoint());
        
        String kode = tabelObat.getValueAt(baris, 0).toString();
        Kode = kode;
        ID.setText(kode);
        
        String kg = tabelObat.getValueAt(baris, 1).toString();
        KD_GOLONGAN.setSelectedItem(kg);
        
        String nm = tabelObat.getValueAt(baris, 2).toString();
        nama.setText(nm);
        
        String jns = tabelObat.getValueAt(baris, 3).toString();
        jenis.setText(jns);
        
        String hrg = tabelObat.getValueAt(baris, 4).toString();
        harga.setText(hrg);
        
        String stk = tabelObat.getValueAt(baris, 5).toString();
        stok.setText(stk);
        
        String kt = tabelObat.getValueAt(baris, 6).toString();
        ket.setText(kt);
    }//GEN-LAST:event_tabelObatMouseClicked

