    public void cbm(){
        String kdgolongan = "";
        int i = 0;
        int kolom = tblGolongan.getRowCount() - 1;
        while(i <= kolom){
            kdgolongan = tblGolongan.getValueAt(i, 0).toString();
            KD_GOLONGAN.addItem(kdgolongan);
            i= i + 1;
        }
    }
    
    public void tampilkanGolongan() {
        DefaultTableModel table = new DefaultTableModel();

        table.addColumn("Kode_Golongan");
        

        try {
            Connection con = connectionmysql.koneksiDB();
            String sql = "select * from golongan_obat";
            Statement S = con.createStatement();
            ResultSet R = S.executeQuery(sql);

            while (R.next()) {
                table.addRow(new Object[]{
                    R.getString(1)
                });
            }
            tblGolongan.setModel(table);

        } catch (Exception e) {
            e.getMessage();
        }
    }
    
    public void tampilkanDataObat() {
        DefaultTableModel table = new DefaultTableModel();

        table.addColumn("Id_Obat");
        table.addColumn("Kode_Golongan");
        table.addColumn("Nama");
        table.addColumn("Jenis");
        table.addColumn("Harga");
        table.addColumn("Stok");
        table.addColumn("Keterangan");

        try {
            Connection con = connectionmysql.koneksiDB();
            String sql = "select * from obat";
            Statement S = con.createStatement();
            ResultSet R = S.executeQuery(sql);

            while (R.next()) {
                table.addRow(new Object[]{
                    R.getString(1), R.getString(2), R.getString(3), R.getString(4), R.getString(5), R.getString(6), R.getString(7)
                });
            }
            tabelObat.setModel(table);

        } catch (Exception e) {
            e.getMessage();
        }
    }
    
